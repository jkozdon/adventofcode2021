from collections import defaultdict


def handle_day(a):
    b = defaultdict(int)
    for c, n in a.items():
        if c == 0:
            b[6] += n
            b[8] += n
        else:
            b[c - 1] += n
    return b


def main(name, input, days):
    vals = defaultdict(int)
    for i in input:
        vals[int(i)] += 1
    for d in range(days):
        vals = handle_day(vals)
    c = 0
    for n in vals.values():
        c += n
    print(name, ":", c)


